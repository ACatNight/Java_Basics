package DayTwo;

public class two {
    public static void main(String[] args) {
        double a = 5;
        double b = 2;
        System.out.println(a + b);
        System.out.println(a - b);
        System.out.println(a * b);
        System.out.println(a / b);
        System.out.println(a % b);
        int i = 5;
        int j = 2;
        System.out.println(1.0 * i / j);
        System.out.println("----------------------");
        //掌握+符号做连接符的情况
        int a2 = 5;
        System.out.println("abc" + a2); //abc5
        System.out.println(a2 + 5); // 10
        System.out.println("ACat" + a2 + "a");
        System.out.println(a2 + 'a' + "Cat");



    }
}
